<?php
    function sumar($x, $y){
        return $x + $y;
    }
    function resta($x, $y){
        return $x - $y;
    }
    function multiplicacion($x, $y){
        return $x * $y;
    }
    function division($x, $y){
        return $x / $y;
    }
    function modulo($x, $y){
        return $x % $y;
    }
    function potencia($x, $y){
        return $x ** $y;
    }
    
?>